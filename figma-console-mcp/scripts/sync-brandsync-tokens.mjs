#!/usr/bin/env node
/**
 * sync-brandsync-tokens.mjs
 *
 * Reads brandsync-tokens CSS, strips the --bs- prefix, and outputs
 * a JSON token map + a ready-to-paste figma_execute script for
 * syncing values into existing Figma variables.
 *
 * Usage:
 *   node scripts/sync-brandsync-tokens.mjs [path-to-tokens.css]
 *
 * Default CSS path resolves from the frontend node_modules.
 * Override with: node scripts/sync-brandsync-tokens.mjs /path/to/tokens.css
 *
 * Output:
 *   - scripts/out/brandsync-tokens.json  (variable name → resolved value)
 *   - scripts/out/figma-sync.js          (paste into figma_execute tool)
 */

import { readFileSync, writeFileSync, mkdirSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));

// ── Config ────────────────────────────────────────────────────────────────────

const DEFAULT_CSS_PATH = resolve(
  __dirname,
  "../../../eg-brandsync/frontend/node_modules/brandsync-tokens/dist/css/tokens.css"
);
const cssPath = process.argv[2] ?? DEFAULT_CSS_PATH;
const outDir = resolve(__dirname, "out");

// ── Parse CSS tokens ──────────────────────────────────────────────────────────

function parseTokens(css) {
  // Strip JS-style // comments (brandsync-tokens emits these)
  const cleaned = css.replace(/^\s*\/\/.*$/gm, "");

  const raw = {};

  // Extract all --bs-* declarations from :root block only (light mode base)
  const rootMatch = cleaned.match(/:root\s*\{([^}]+)\}/);
  if (!rootMatch) {
    console.error("Could not find :root block in tokens.css");
    process.exit(1);
  }

  const rootBlock = rootMatch[1];
  const varRegex = /--bs-([\w-]+)\s*:\s*([^;]+);/g;
  let m;
  while ((m = varRegex.exec(rootBlock)) !== null) {
    const name = m[1].trim();           // without bs- prefix
    const value = m[2].trim();
    raw[name] = value;
  }

  return raw;
}

function resolveValues(raw) {
  // Resolve var(--bs-*) references to actual values (max 5 hops)
  const resolved = { ...raw };

  for (let pass = 0; pass < 5; pass++) {
    let changed = false;
    for (const [name, value] of Object.entries(resolved)) {
      const ref = value.match(/^var\(--bs-([\w-]+)\)$/);
      if (ref && resolved[ref[1]] && !resolved[ref[1]].startsWith("var(")) {
        resolved[name] = resolved[ref[1]];
        changed = true;
      }
    }
    if (!changed) break;
  }

  return resolved;
}

// ── Categorise ────────────────────────────────────────────────────────────────

function categorise(tokens) {
  const colors = {};
  const spacing = {};
  const typography = {};
  const borderRadius = {};
  const other = {};

  for (const [name, value] of Object.entries(tokens)) {
    if (value.startsWith("#") || value.startsWith("rgb") || value.startsWith("hsl")) {
      colors[name] = value;
    } else if (name.startsWith("spacing-")) {
      spacing[name] = value;
    } else if (name.startsWith("font-") || name.startsWith("line-height-") || name.startsWith("letter-spacing-")) {
      typography[name] = value;
    } else if (name.startsWith("border-radius-")) {
      borderRadius[name] = value;
    } else {
      other[name] = value;
    }
  }

  return { colors, spacing, typography, borderRadius, other };
}

// ── Generate figma_execute script ─────────────────────────────────────────────

function generateFigmaScript(tokens) {
  return `// BrandSync token sync — generated by sync-brandsync-tokens.mjs
// Paste this into the figma_execute tool to update variable values.
// Prerequisites: Figma file must already have variables named without the bs- prefix
//   e.g. Figma variable "color-primary-default" ← CSS "--bs-color-primary-default"

const TOKEN_MAP = ${JSON.stringify(tokens, null, 2)};

const collections = figma.variables.getLocalVariableCollections();
let updated = 0;
let notFound = 0;

for (const [tokenName, tokenValue] of Object.entries(TOKEN_MAP)) {
  // Find matching variable across all collections
  // Figma variable names may use "/" as separator instead of "-"
  // Try both formats: "color-primary-default" and "color/primary/default"
  const slashName = tokenName.replace(/-/g, "/");

  let variable = null;
  for (const collection of collections) {
    const vars = collection.variableIds.map(id => figma.variables.getVariableById(id));
    variable = vars.find(v => v && (v.name === tokenName || v.name === slashName));
    if (variable) break;
  }

  if (!variable) {
    notFound++;
    continue;
  }

  // Get default mode id
  const collection = figma.variables.getVariableCollectionById(variable.variableCollectionId);
  const modeId = collection.defaultModeId;

  try {
    if (variable.resolvedType === "COLOR") {
      // Parse hex to RGB
      const hex = tokenValue.replace("#", "");
      const r = parseInt(hex.slice(0, 2), 16) / 255;
      const g = parseInt(hex.slice(2, 4), 16) / 255;
      const b = parseInt(hex.slice(4, 6), 16) / 255;
      const a = hex.length === 8 ? parseInt(hex.slice(6, 8), 16) / 255 : 1;
      variable.setValueForMode(modeId, { r, g, b, a });
    } else {
      // FLOAT or STRING
      const num = parseFloat(tokenValue);
      variable.setValueForMode(modeId, isNaN(num) ? tokenValue : num);
    }
    updated++;
  } catch (e) {
    // Skip variables that can't be updated
    notFound++;
  }
}

return { updated, notFound, total: Object.keys(TOKEN_MAP).length };
`;
}

// ── Main ──────────────────────────────────────────────────────────────────────

console.log(`Reading tokens from: ${cssPath}`);
const css = readFileSync(cssPath, "utf8");

const raw = parseTokens(css);
console.log(`Parsed ${Object.keys(raw).length} raw tokens`);

const resolved = resolveValues(raw);
const unresolved = Object.values(resolved).filter(v => v.startsWith("var(")).length;
console.log(`Resolved to ${Object.keys(resolved).length} tokens (${unresolved} still referencing vars)`);

const categorised = categorise(resolved);
console.log(`  Colors: ${Object.keys(categorised.colors).length}`);
console.log(`  Spacing: ${Object.keys(categorised.spacing).length}`);
console.log(`  Typography: ${Object.keys(categorised.typography).length}`);
console.log(`  Border radius: ${Object.keys(categorised.borderRadius).length}`);
console.log(`  Other: ${Object.keys(categorised.other).length}`);

// Write outputs
mkdirSync(outDir, { recursive: true });

const jsonOut = resolve(outDir, "brandsync-tokens.json");
writeFileSync(jsonOut, JSON.stringify({ meta: { source: cssPath, generatedAt: new Date().toISOString(), totalTokens: Object.keys(resolved).length }, tokens: resolved, categorised }, null, 2));
console.log(`\nJSON written → ${jsonOut}`);

const figmaScript = generateFigmaScript(resolved);
const jsOut = resolve(outDir, "figma-sync.js");
writeFileSync(jsOut, figmaScript);
console.log(`Figma sync script written → ${jsOut}`);
console.log(`\nNext: paste contents of figma-sync.js into the figma_execute tool`);
